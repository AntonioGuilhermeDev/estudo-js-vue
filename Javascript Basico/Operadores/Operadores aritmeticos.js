// Operadores matemáticos


let number1, number2, result;

// +

number1 =5;
number2 =10;
result  = number1 + number2;

console.log(result);

// -

number1 =15;
number2 =20;
result = number1 - number2;

console.log(result);

// *

number1 =7;
number2 =2;
result = number1 * number2;

console.log(result);

//  /
number1 =8;
number2 =2;
result = number1 / number2;

console.log(result);

// %

number1 =9;
number2 =2;
result = number1 % number2;

console.log(result);

// Ordem de operações

number1 =17;
number2 =2;
result = (number1 + number2) * 3;

console.log(result);


